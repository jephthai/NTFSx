module TypeSyms where

-- These are some synonyms just so you can tell easily what
-- size the fields are in the entry.  It's just a convenience.

type MByte = Int  -- 8 bits
type MInt  = Int  -- 16 bits
type MWord = Int  -- 32 bits
type MLong = Int  -- 64 bits

